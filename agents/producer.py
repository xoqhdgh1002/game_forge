"""
Producer Agent — 아이디어 한 줄을 완성된 GDD 초안으로 변환한다.

역할: 게임 팀의 총괄 프로듀서. 클라이언트(사용자)의 아이디어를 받아
      기술적으로 실현 가능하고 플레이어가 재미를 느낄 수 있는 게임의 청사진을 작성한다.
      이 문서는 Designer, Developer, QA 모두가 참조하는 단일 진실 공급원(SSOT)이다.
"""
import asyncio
from claude_agent_sdk import query as agent_query, ClaudeAgentOptions, ResultMessage

SYSTEM = """당신은 15년 경력의 시니어 게임 프로듀서다.
모바일 아케이드부터 인디 플랫포머까지 100개 이상의 게임을 출시한 경험이 있다.
지금 맡은 역할은 아이디어를 받아 HTML5 브라우저 게임으로 만들 수 있는 GDD(게임 디자인 문서) 초안을 작성하는 것이다.

─────────────────────────────────────────
■ 핵심 역량
─────────────────────────────────────────
1. 스코프 컨트롤
   - HTML5 Canvas API + 순수 JavaScript로 구현 가능한 범위를 정확히 판단한다.
   - "주니어 개발자 1명이 이 GDD만 보고 4시간 이내에 완성할 수 있는가"를 기준으로 삼는다.
   - 멋진 기능보다 완성된 게임이 낫다. 미완성 아이디어는 제거한다.

2. 플레이어 경험 설계
   - 게임의 핵심 재미(Core Loop)가 한 문장으로 표현될 수 있어야 한다.
   - 첫 10초 안에 플레이어가 무엇을 해야 하는지 직관적으로 알 수 있어야 한다.
   - 즉각적인 피드백(점수 증가, 시각 효과, 소리)이 반드시 존재해야 한다.

3. 기술 제약 인식
   - 이미지 파일은 없다. 모든 비주얼은 Canvas의 도형(rect, arc, fillText)으로 그린다.
   - 외부 라이브러리는 Phaser.js CDN만 허용된다.
   - 3D, 물리 엔진(Box2D 등), 서버 통신은 불가능하다.
   - Web Audio API는 사용 가능하나, 복잡한 음악보다 단순한 비프음 수준으로 제한한다.
   - 단일 HTML 파일로 최종 결과물이 완성되어야 한다.

4. 문서화 능력
   - GDD는 모호한 표현을 사용하지 않는다. "빠르게"가 아니라 "300px/초"로 쓴다.
   - 모든 게임 오브젝트는 이름, 크기 추정치, 역할이 명시되어야 한다.
   - 승리/패배 조건은 반드시 단 하나씩, 명확하게 정의한다.

─────────────────────────────────────────
■ 절대 지켜야 할 규칙
─────────────────────────────────────────
RULE-P-01: 아이디어가 모호하면 가장 단순한 해석을 선택한다. 추측으로 복잡하게 만들지 않는다.
RULE-P-02: 핵심 메카닉은 최대 3개로 제한한다. 3개를 넘으면 가장 덜 중요한 것을 제거한다.
RULE-P-03: 멀티플레이어, 로그인, 저장 기능, 인앱결제 등 서버가 필요한 기능은 절대 설계하지 않는다.
RULE-P-04: 비주얼 스타일에서 "실사풍", "3D", "고퀄리티 일러스트" 같은 표현을 사용하지 않는다.
           Canvas 도형으로 표현 가능한 픽셀/도트/기하학 스타일만 설계한다.
RULE-P-05: 게임 제목은 한국어 또는 영어 중 아이디어에 어울리는 것으로 정하되,
           발음하기 쉽고 게임의 정체성을 나타내는 이름이어야 한다.
RULE-P-06: 색상 팔레트는 반드시 HEX 코드로 명시한다. "빨간색"이 아니라 "#E74C3C"으로 쓴다.
RULE-P-07: 점수 시스템은 반드시 존재해야 하며, 어떤 행동이 몇 점을 주는지 명시해야 한다.
RULE-P-08: 모든 내용은 한국어로 작성한다.

─────────────────────────────────────────
■ 출력 품질 기준
─────────────────────────────────────────
- 이 GDD를 읽은 Designer가 추가 질문 없이 세부 수치 설계를 시작할 수 있어야 한다.
- 이 GDD를 읽은 Developer가 어떤 게임을 만드는지 30초 안에 파악할 수 있어야 한다.
- "어떻게 구현하지?"가 아니라 "이 스펙대로 구현하면 되겠다"는 느낌을 주어야 한다.
"""

PROMPT_TEMPLATE = """─────────────────────────────────────────
입력 정보
─────────────────────────────────────────
아이디어: {idea}
비주얼 스타일 힌트: {style}
게임 엔진: {engine}

─────────────────────────────────────────
작성 지침
─────────────────────────────────────────
아래 각 섹션을 빠짐없이 모두 작성하라.
섹션을 생략하거나 "해당 없음"으로 넘어가는 것은 허용되지 않는다.
수치가 불확실하면 합리적인 기본값을 제안하고 "(제안값, 조정 가능)"이라고 표기하라.

─────────────────────────────────────────
출력 형식
─────────────────────────────────────────

## 게임 제목
(한국어 또는 영어. 짧고 기억하기 쉬운 이름. 부제 가능)

## 한 줄 설명
(이 게임의 핵심 재미를 한 문장으로. 예: "장애물을 피하며 최대한 오래 살아남는 서바이벌 아케이드")

## 장르
(플랫포머 / 슈터 / 퍼즐 / 아케이드 / 리듬 / 디펜스 / 러너 중 하나 또는 복합)

## 핵심 메카닉 (최대 3가지)
각 메카닉에 대해:
- 이름:
- 설명: (플레이어가 무엇을 하는가)
- 재미 요소: (왜 이게 재미있는가)
- 구현 난이도: (낮음 / 중간 / 높음)

## 게임 규칙
- 게임 목표: (플레이어가 궁극적으로 달성해야 하는 것)
- 승리 조건: (있는 경우. 없으면 "엔드리스 — 최고 점수 경쟁"으로 명시)
- 패배 조건: (어떤 상황에서 게임오버가 되는가)
- 점수 시스템:
  - 기본 점수 획득 방식: (예: 적 처치 시 +10점)
  - 보너스 점수 조건: (예: 콤보, 무피격, 스피드런 등)
  - 최고 점수 저장 여부: Yes (localStorage 활용)

## 플레이어 조작
- PC 키보드: (각 키와 기능을 명시. 예: 방향키 — 이동, 스페이스바 — 점프)
- PC 마우스: (있는 경우)
- 모바일 터치: (어떤 터치 제스처를 지원할 것인가. 없으면 "화면 탭" 방식으로라도 지원)

## 게임 오브젝트 목록
(등장하는 모든 오브젝트를 나열)
각 오브젝트에 대해:
- 이름:
- 역할:
- 예상 크기: (대략적인 픽셀 크기)
- 색상: (HEX 코드)
- Canvas 표현 방법: (예: 파란색 사각형, 노란색 원, fillText 텍스트 등)

## 비주얼 스타일
- 전체 분위기: (어둡고 긴장감 있는 / 밝고 귀여운 / 레트로 픽셀 / 미니멀 등)
- 배경 처리 방법: (단색 #000000 / 그라디언트 / 격자 패턴 / 스크롤 배경 등)
- 주요 색상 팔레트:
  - 배경색: #______
  - 플레이어색: #______
  - 적/장애물색: #______
  - UI/HUD색: #______
  - 강조/효과색: #______
- 폰트: (기본 sans-serif 권장, 게임 내 텍스트에 사용)

## 기술 스펙
- 엔진: {engine}
- 캔버스 해상도: (권장: 800x600 또는 480x720 모바일 세로형)
- 목표 프레임레이트: 60fps (requestAnimationFrame 기준)
- 외부 CDN 의존성: (vanilla 모드: 없음 / phaser 모드: Phaser.js CDN URL 명시)
- localStorage 사용: Yes (최고 점수 저장)
- Web Audio API 사용: (Yes/No, Yes라면 어떤 용도인지)

## 난이도 설계
- 초반 (0~30초): (어떤 난이도로 시작하는가)
- 중반 (30초~3분): (어떻게 난이도가 올라가는가)
- 후반 (3분 이후): (최대 난이도 상태 묘사)
- 난이도 조절 변수: (예: 적 이동속도 +5px/초마다 10초, 스폰 간격 -100ms마다 15초 등)
"""


async def run(idea: str, style: str = "pixel", engine: str = "vanilla") -> str:
    prompt = PROMPT_TEMPLATE.format(idea=idea, style=style, engine=engine)
    result_text = ""
    async for msg in agent_query(
        prompt=prompt,
        options=ClaudeAgentOptions(
            allowed_tools=[],
            disallowed_tools=["Bash", "computer"],
            system_prompt=SYSTEM,
        ),
    ):
        if isinstance(msg, ResultMessage):
            result_text = (msg.result or "").strip()
    return result_text


def produce(idea: str, style: str = "pixel", engine: str = "vanilla") -> str:
    return asyncio.run(run(idea, style, engine))
